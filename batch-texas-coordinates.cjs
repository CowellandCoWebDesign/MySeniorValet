async function batchUpdateTexasCoordinates() {
  console.log('🤠 BATCH UPDATING TEXAS COORDINATES');
  
  const batchSize = 25;
  let totalUpdated = 0;
  let offset = 0;
  
  while (true) {
    // Get batch of Texas facilities without coordinates
    const query = `
      SELECT id, name, city, county, state, latitude, longitude
      FROM communities 
      WHERE state = 'TX' 
      AND (latitude IS NULL OR longitude IS NULL)
      ORDER BY city, name
      LIMIT $1 OFFSET $2
    `;
    
    const result = await pool.query(query, [batchSize, offset]);
    const facilities = result.rows;
    
    if (facilities.length === 0) {
      break; // No more facilities to process
    }
    
    console.log(`Processing batch ${Math.floor(offset / batchSize) + 1}: ${facilities.length} facilities`);
    
    let batchUpdated = 0;
    
    for (const facility of facilities) {
      const coordinates = TEXAS_COORDINATES[facility.city];
      
      if (coordinates) {
        // Add small random offset to avoid overlapping markers
        const latOffset = (Math.random() - 0.5) * 0.02;
        const lngOffset = (Math.random() - 0.5) * 0.02;
        
        const finalLat = coordinates.lat + latOffset;
        const finalLng = coordinates.lng + lngOffset;
        
        try {
          const updateQuery = `
            UPDATE communities 
            SET latitude = $1, longitude = $2
            WHERE id = $3
          `;
          
          await pool.query(updateQuery, [finalLat, finalLng, facility.id]);
          batchUpdated++;
          
        } catch (error) {
          console.error(`❌ Failed to update ${facility.name}: ${error.message}`);
        }
      }
    }
    
    totalUpdated += batchUpdated;
    console.log(`✅ Batch complete: ${batchUpdated} updated (Total: ${totalUpdated})`);
    
    offset += batchSize;
    
    // Small delay to prevent overwhelming the database
    await new Promise(resolve => setTimeout(resolve, 100));
  }
  
  console.log(`\n🎯 BATCH UPDATE COMPLETE: ${totalUpdated} facilities updated`);
  
  // Final verification
  const verifyQuery = `
    SELECT 
      COUNT(*) as total,
      COUNT(CASE WHEN latitude IS NOT NULL AND longitude IS NOT NULL THEN 1 END) as with_coordinates
    FROM communities 
    WHERE state = 'TX'
  `;
  
  const verifyResult = await pool.query(verifyQuery);
  const stats = verifyResult.rows[0];
  
  console.log(`\n🗺️ FINAL TEXAS MAP STATUS:`);
  console.log(`   Total facilities: ${stats.total}`);
  console.log(`   With coordinates: ${stats.with_coordinates}`);
  console.log(`   Coverage: ${((stats.with_coordinates / stats.total) * 100).toFixed(1)}%`);
  
  if (stats.with_coordinates > 1000) {
    console.log(`\n🤠 SUCCESS: Texas facilities visible on map!`);
  }
  
  await pool.end();
}

